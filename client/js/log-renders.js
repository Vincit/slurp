/**
 * Run this function for example in web console to start logging all
 * renderings of Meteor templates. This can then be used for tracking,
 * which templates are rerendered.
 */
logRenders = function() {
  _.each(Template, function (template, name) {
    var oldRender = template.rendered;
    var counter = 0;

    template.rendered = function () {
      console.log(name, "render count: ", ++counter);
      oldRender && oldRender.apply(this, arguments);
    };
  });
};
